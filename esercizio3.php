/* Create 4 variabili: $x = 10; $y = 20; $z = "20"; $w = 10; ed utilizzando var_dump() per visualizzare l'esito dei seguenti confronti: 
  $x < $y [esempio: var_dump($x < $y); ] 
  $x <= $w 
  $y == $z 
  $y === $z 
